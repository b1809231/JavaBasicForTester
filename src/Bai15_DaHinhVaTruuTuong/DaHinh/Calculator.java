package Bai15_DaHinhVaTruuTuong.DaHinh;

public class Calculator {

    public int tinhTong(int num1, int num2){
        return num1 + num2;
    }

    public int tinhTong(int num1, int num2, int num3){
        return num1 + num2 + num3;
    }

    public double phepNhan(int num1, int num2){
        return num1*num2;
    }

    public double phepNhan(int num1, float num2){
        return num1*num2;
    }
}
