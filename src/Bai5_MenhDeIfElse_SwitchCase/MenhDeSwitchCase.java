package Bai5_MenhDeIfElse_SwitchCase;

public class MenhDeSwitchCase {
    public static void main(String[] args) {
        int number = 29;
        switch (number){
            case 10:
                System.out.println("10");
                break;
            case 20:
                System.out.println("20");
                break;
            case 30:
                System.out.println("30");
            default:
                System.out.println("Khong co gia tri nao dung");
        }
    }
}

